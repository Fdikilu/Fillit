#include "fillit.h"

int			min_width(int nb_tetri)
{
	int		i;

	i = 2;
	while (i <= 10)
	{
		if ((nb_tetri + 1) * 4 <= i * i)
			return (i);
		i++;
	}
	return (i);
}
